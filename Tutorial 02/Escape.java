// Program to print Escape Characters.

public class Escape{
	public static void main (String []args){
		System.out.println("Escape sequence is a sequence of character which begins with \\.");
		System.out.println("The character following \\ will not represent itself, rather give a special meaning.");
		System.out.println("For example \'\\t\' represents tab space but not the letter \'t\' itself.");
	}
}
