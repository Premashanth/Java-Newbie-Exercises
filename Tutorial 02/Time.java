//Program Time prints hours in seconds.

public class Time {
	public static final double SECONDS = 60;
	public static void main (String []args){
		System.out.println ("One hour is" + (SECONDS*60*1) +"seconds.");
		System.out.println ("Ten hour is" + (SECONDS*60*10) +"seconds.");
		System.out.println ("One hundred hour is" + (SECONDS*60*100) +"seconds.");
	}
}